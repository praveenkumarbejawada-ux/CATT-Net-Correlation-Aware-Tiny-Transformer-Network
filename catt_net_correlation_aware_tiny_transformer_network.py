def create_supervised_sequences(
    X_all_raw: np.ndarray,
    y_all: np.ndarray,
    seq_len: int,
    horizon: int,
    scaler_x: MinMaxScaler,
) -> Tuple[np.ndarray, np.ndarray]:
    X_all = scaler_x.transform(X_all_raw.astype(np.float32))
    X_seq, y_seq = [], []
    limit = len(X_all) - seq_len - horizon + 1
    for i in range(limit):
        X_seq.append(X_all[i:i + seq_len])
        y_seq.append(y_all[i + seq_len + horizon - 1])
    return np.asarray(X_seq, dtype=np.float32), np.asarray(y_seq, dtype=np.float32)


def split_sequences(X: np.ndarray, y: np.ndarray) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    n = len(X)
    n_train = int(TRAIN_RATIO * n)
    n_val = int(VAL_RATIO * n)

    X_train, y_train = X[:n_train], y[:n_train]
    X_val, y_val = X[n_train:n_train + n_val], y[n_train:n_train + n_val]
    X_test, y_test = X[n_train + n_val:], y[n_train + n_val:]
    return X_train, y_train, X_val, y_val, X_test, y_test

