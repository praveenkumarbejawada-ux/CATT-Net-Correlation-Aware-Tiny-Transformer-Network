def evaluate_linear_regression_baseline(
    X_train: np.ndarray,
    y_train: np.ndarray,
    X_test: np.ndarray,
    y_test: np.ndarray,
    scaler_y: MinMaxScaler,
    target_cols: List[str],
    save_dir: str,
) -> Dict:
    X_train_flat = X_train.reshape(X_train.shape[0], -1)
    X_test_flat = X_test.reshape(X_test.shape[0], -1)

    model = MultiOutputRegressor(LinearRegression())
    model.fit(X_train_flat, y_train)
    y_pred_norm = model.predict(X_test_flat)

    y_true = inverse_transform_targets(y_test, scaler_y)
    y_pred = inverse_transform_targets(y_pred_norm, scaler_y)
    metrics = evaluate_per_target(y_true, y_pred, target_cols)

    with open(os.path.join(save_dir, "linear_regression_metrics.json"), "w") as f:
        json.dump(metrics, f, indent=2)

    print("\n===== LinearRegression FP32-ONLY METRICS =====")
    print(json.dumps(metrics, indent=2))
    return metrics


def run_baseline_comparison(
    target_cols: List[str],
    scaler_y: MinMaxScaler,
    X_train: np.ndarray,
    y_train: np.ndarray,
    X_val: np.ndarray,
    y_val: np.ndarray,
    X_test: np.ndarray,
    y_test: np.ndarray,
    proposed_fp32_metrics: Dict,
    proposed_int8_metrics: Optional[Dict],
) -> None:
    print("\n[INFO] Running baseline comparison with INT8 export...")
    baseline_dir = os.path.join(OUT_DIR, "baseline_comparison")
    int8_dir = os.path.join(baseline_dir, "int8_models")
    os.makedirs(baseline_dir, exist_ok=True)
    os.makedirs(int8_dir, exist_ok=True)

    results_fp32 = []
    results_int8 = []

    linear_metrics = evaluate_linear_regression_baseline(X_train, y_train, X_test, y_test, scaler_y, target_cols, baseline_dir)
    results_fp32.append({"variant": "LinearRegression", "epochs_ran": 0, **linear_metrics["macro_avg"]})

    keras_baselines = [
        ("CNN1D", build_cnn_baseline),
        ("LSTM", build_lstm_baseline),
        ("GRU", build_gru_baseline),
    ]

    for name, builder in keras_baselines:
        tf.keras.backend.clear_session()
        set_global_determinism(SEED)
        model = builder(X_train.shape[1], X_train.shape[2], len(target_cols))
        model, history = train_keras_model(model, X_train, y_train, X_val, y_val, BASELINE_EPOCHS, BATCH_SIZE, verbose=0)

        with open(os.path.join(baseline_dir, f"{name.lower()}_history.json"), "w") as f:
            json.dump(history, f, indent=2)

        fp32_metrics, int8_metrics = save_keras_and_int8_results(
            model=model,
            model_name=name.lower(),
            X_train=X_train,
            X_test=X_test,
            y_test=y_test,
            scaler_y=scaler_y,
            target_cols=target_cols,
            save_dir=int8_dir,
        )

        results_fp32.append({"variant": name, "epochs_ran": len(history["loss"]), **fp32_metrics["macro_avg"]})
        if int8_metrics is not None:
            results_int8.append({"variant": f"{name}_INT8", "epochs_ran": len(history["loss"]), **int8_metrics["macro_avg"]})

    results_fp32.append({"variant": "Proposed_ImprovedTinyTransformer", "epochs_ran": -1, **proposed_fp32_metrics["macro_avg"]})
    if proposed_int8_metrics is not None:
        results_int8.append({"variant": "Proposed_ImprovedTinyTransformer_INT8", "epochs_ran": -1, **proposed_int8_metrics["macro_avg"]})

    df_fp32 = pd.DataFrame(results_fp32).sort_values(by="RMSE")
    df_fp32.to_csv(os.path.join(baseline_dir, "baseline_results_fp32.csv"), index=False)
    plot_results_bar(df_fp32, baseline_dir, prefix="Baseline Comparison FP32")

    print("\n===== BASELINE COMPARISON FP32 RESULTS =====")
    print(df_fp32.to_string(index=False))

    if results_int8:
        df_int8 = pd.DataFrame(results_int8).sort_values(by="RMSE")
        df_int8.to_csv(os.path.join(baseline_dir, "baseline_results_int8.csv"), index=False)
        plot_results_bar(df_int8, baseline_dir, prefix="Baseline Comparison INT8")
        print("\n===== BASELINE COMPARISON INT8 RESULTS =====")
        print(df_int8.to_string(index=False))

