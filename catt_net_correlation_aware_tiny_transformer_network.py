def coarse_fill_matrix(X_missing: np.ndarray, feature_names: List[str], sentinel: float = -1.0) -> np.ndarray:
    df = pd.DataFrame(X_missing.copy(), columns=feature_names)
    pollutant_subset = [c for c in POLLUTANT_INPUT_COLS if c in df.columns]
    if pollutant_subset:
        df[pollutant_subset] = df[pollutant_subset].replace(sentinel, np.nan)
        df[pollutant_subset] = df[pollutant_subset].interpolate(limit_direction="both").ffill().bfill().fillna(0.0)
    return df.values.astype(np.float32)


def batch_corr(x: tf.Tensor) -> tf.Tensor:
    x_center = x - tf.reduce_mean(x, axis=0, keepdims=True)
    cov = tf.matmul(x_center, x_center, transpose_a=True) / (tf.cast(tf.shape(x)[0], tf.float32) - 1.0 + 1e-8)
    std = tf.sqrt(tf.maximum(tf.linalg.diag_part(cov), 1e-8))
    denom = tf.tensordot(std, std, axes=0) + 1e-8
    return cov / denom


def build_generator(latent_dim: int, output_dim: int, hidden_dim: int = 128) -> Model:
    z = layers.Input(shape=(latent_dim,))
    x = layers.Dense(hidden_dim, activation="relu")(z)
    x = layers.Dense(hidden_dim, activation="relu")(x)
    out = layers.Dense(output_dim, activation="sigmoid")(x)
    return Model(z, out, name="generator")


def build_discriminator(input_dim: int, hidden_dim: int = 128) -> Model:
    x_in = layers.Input(shape=(input_dim,))
    x = layers.Dense(hidden_dim, activation="relu")(x_in)
    x = layers.Dense(hidden_dim // 2, activation="relu")(x)
    out = layers.Dense(1, activation="sigmoid")(x)
    return Model(x_in, out, name="discriminator")


class VanillaCandidateGAN:
    def __init__(self, input_dim: int, latent_dim: int = 32, corr_lambda: float = 0.0):
        self.latent_dim = latent_dim
        self.corr_lambda = corr_lambda
        self.generator = build_generator(latent_dim, input_dim)
        self.discriminator = build_discriminator(input_dim)
        self.g_opt = tf.keras.optimizers.Adam(1e-3)
        self.d_opt = tf.keras.optimizers.Adam(1e-3)
        self.bce = tf.keras.losses.BinaryCrossentropy()

    @tf.function
    def train_step(self, x_real: tf.Tensor) -> Tuple[tf.Tensor, tf.Tensor]:
        batch_size = tf.shape(x_real)[0]
        z = tf.random.normal((batch_size, self.latent_dim))

        with tf.GradientTape() as d_tape, tf.GradientTape() as g_tape:
            x_fake = self.generator(z, training=True)
            d_real = self.discriminator(x_real, training=True)
            d_fake = self.discriminator(x_fake, training=True)
            d_loss = self.bce(tf.ones_like(d_real), d_real) + self.bce(tf.zeros_like(d_fake), d_fake)

            g_fake = self.discriminator(x_fake, training=True)
            g_loss_adv = self.bce(tf.ones_like(g_fake), g_fake)
            if self.corr_lambda > 0.0:
                corr_real = batch_corr(x_real)
                corr_fake = batch_corr(x_fake)
                corr_loss = tf.reduce_mean(tf.square(corr_real - corr_fake))
            else:
                corr_loss = 0.0
            g_loss = g_loss_adv + self.corr_lambda * corr_loss

        d_grads = d_tape.gradient(d_loss, self.discriminator.trainable_variables)
        g_grads = g_tape.gradient(g_loss, self.generator.trainable_variables)
        self.d_opt.apply_gradients(zip(d_grads, self.discriminator.trainable_variables))
        self.g_opt.apply_gradients(zip(g_grads, self.generator.trainable_variables))
        return d_loss, g_loss

    def fit(self, X_train: np.ndarray, epochs: int = 100, batch_size: int = 64, tag: str = "GAN") -> None:
        dataset = tf.data.Dataset.from_tensor_slices(X_train.astype(np.float32))
        dataset = dataset.shuffle(min(4096, len(X_train)), seed=SEED).batch(batch_size)

        for epoch in range(epochs):
            d_vals, g_vals = [], []
            for x_real in dataset:
                d_loss, g_loss = self.train_step(x_real)
                d_vals.append(float(d_loss.numpy()))
                g_vals.append(float(g_loss.numpy()))
            if (epoch + 1) % 20 == 0 or epoch == 0:
                print(f"[{tag}] Epoch {epoch + 1:03d}/{epochs} | D={np.mean(d_vals):.4f} | G={np.mean(g_vals):.4f}")

    def sample(self, n_samples: int) -> np.ndarray:
        z = np.random.normal(size=(n_samples, self.latent_dim)).astype(np.float32)
        return np.clip(self.generator.predict(z, verbose=0), 0.0, 1.0)


def mesh_candidate_imputation(
    X_missing: np.ndarray,
    mask: np.ndarray,
    candidates: np.ndarray,
    top_k: int = 20,
) -> np.ndarray:
    imputed = X_missing.copy()
    for i in range(len(X_missing)):
        obs = mask[i] == 1
        miss = mask[i] == 0
        if miss.sum() == 0:
            continue

        if obs.sum() == 0:
            top_idx = np.random.choice(len(candidates), size=min(top_k, len(candidates)), replace=False)
            chosen = candidates[top_idx]
            imputed[i, miss] = chosen[:, miss].mean(axis=0)
            continue

        dists = np.sum((candidates[:, obs] - X_missing[i, obs]) ** 2, axis=1)
        top_idx = np.argsort(dists)[: min(top_k, len(candidates))]
        chosen = candidates[top_idx]
        weights = 1.0 / (dists[top_idx] + 1e-6)
        weights = weights / weights.sum()
        imputed[i, miss] = np.sum(chosen[:, miss] * weights[:, None], axis=0)

    return np.clip(imputed, 0.0, 1.0)


class CorrGANStyleImputer:
    def __init__(self, input_dim: int, latent_dim: int = 32, corr_lambda: float = 5.0, pool_size: int = 4000, top_k: int = 20):
        self.gan = VanillaCandidateGAN(input_dim=input_dim, latent_dim=latent_dim, corr_lambda=corr_lambda)
        self.pool_size = pool_size
        self.top_k = top_k

    def fit(self, X_missing: np.ndarray, mask: np.ndarray, feature_names: List[str], epochs: int = 100, batch_size: int = 64) -> None:
        x_real = coarse_fill_matrix(X_missing, feature_names, sentinel=MISSING_SENTINEL)
        self.gan.fit(x_real, epochs=epochs, batch_size=batch_size, tag="CorrGANStyle")

    def transform(self, X_missing: np.ndarray, mask: np.ndarray) -> np.ndarray:
        candidates = self.gan.sample(self.pool_size)
        return mesh_candidate_imputation(X_missing, mask, candidates, top_k=self.top_k)


def preprocess_with_corrgan(
    df: pd.DataFrame,
    pollutant_cols: List[str],
    target_cols: List[str],
) -> Tuple[pd.DataFrame, np.ndarray, np.ndarray, MinMaxScaler, MinMaxScaler, Dict]:
    pollutant_df = clip_outliers(df[pollutant_cols].copy(), pollutant_cols, CLIP_LOWER_Q, CLIP_UPPER_Q)

    coarse_pollutants = pollutant_df.interpolate(limit_direction="both").ffill().bfill().fillna(0.0)
    coarse_feature_df = pd.concat(
        [coarse_pollutants.reset_index(drop=True), df[TIME_FEATURES].reset_index(drop=True)],
        axis=1,
    )

    scaler_x = MinMaxScaler()
    X_full_norm = scaler_x.fit_transform(coarse_feature_df[INPUT_FEATURES].values.astype(np.float32))

    X_missing_norm = X_full_norm.copy()
    mask = np.ones_like(X_full_norm, dtype=np.float32)

    for col in pollutant_cols:
        idx = INPUT_FEATURES.index(col)
        missing_rows = pollutant_df[col].isna().values
        X_missing_norm[missing_rows, idx] = MISSING_SENTINEL
        mask[missing_rows, idx] = 0.0

    print("[INFO] Training CorrGANStyle imputer for real missing-value completion...")
    corrgan = CorrGANStyleImputer(
        input_dim=len(INPUT_FEATURES),
        latent_dim=CORRGAN_LATENT_DIM,
        corr_lambda=CORR_LAMBDA,
        pool_size=CORRGAN_POOL_SIZE,
        top_k=CORRGAN_TOPK,
    )
    corrgan.fit(X_missing_norm, mask, INPUT_FEATURES, epochs=CORRGAN_EPOCHS, batch_size=CORRGAN_BATCH_SIZE)
    X_imp_norm = corrgan.transform(X_missing_norm, mask)
    X_imp = scaler_x.inverse_transform(X_imp_norm)

    feature_df = pd.DataFrame(X_imp, columns=INPUT_FEATURES)
    y_raw = np.clip(feature_df[target_cols].values.astype(np.float32), a_min=0.0, a_max=None)
    y_log = np.log1p(y_raw)
    scaler_y = MinMaxScaler()
    y_norm = scaler_y.fit_transform(y_log)

    preprocess_info = {
        "preprocessing": "CorrGANStyle_missing_value_imputation",
        "corrgan_epochs": CORRGAN_EPOCHS,
        "corrgan_batch_size": CORRGAN_BATCH_SIZE,
        "corrgan_pool_size": CORRGAN_POOL_SIZE,
        "corrgan_top_k": CORRGAN_TOPK,
        "input_features": INPUT_FEATURES,
        "target_cols": target_cols,
        "forecast_seq_len": FORECAST_SEQ_LEN,
        "target_transform": "log1p_then_minmax",
    }

    return feature_df, feature_df[INPUT_FEATURES].values.astype(np.float32), y_norm, scaler_x, scaler_y, preprocess_info

