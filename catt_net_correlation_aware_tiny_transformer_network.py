def smape(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    denom = np.abs(y_true) + np.abs(y_pred) + 1e-8
    return float(100.0 * np.mean(2.0 * np.abs(y_pred - y_true) / denom))


def evaluate_per_target(y_true: np.ndarray, y_pred: np.ndarray, target_cols: List[str]) -> Dict:
    results = {}
    mae_list, rmse_list, r2_list, smape_list = [], [], [], []

    for idx, col in enumerate(target_cols):
        yt = y_true[:, idx]
        yp = y_pred[:, idx]
        mae = float(mean_absolute_error(yt, yp))
        rmse = float(np.sqrt(mean_squared_error(yt, yp)))
        r2 = float(r2_score(yt, yp))
        smape_val = smape(yt, yp)

        results[col] = {
            "MAE": mae,
            "RMSE": rmse,
            "R2": r2,
            "sMAPE_percent": smape_val,
        }
        mae_list.append(mae)
        rmse_list.append(rmse)
        r2_list.append(r2)
        smape_list.append(smape_val)

    results["macro_avg"] = {
        "MAE": float(np.mean(mae_list)),
        "RMSE": float(np.mean(rmse_list)),
        "R2": float(np.mean(r2_list)),
        "sMAPE_percent": float(np.mean(smape_list)),
    }
    return results


def metrics_dict_to_dataframe(metrics_dict: Dict) -> pd.DataFrame:
    rows = []
    for name, vals in metrics_dict.items():
        row = {"target": name}
        row.update(vals)
        rows.append(row)
    return pd.DataFrame(rows)


def inverse_transform_targets(y_norm: np.ndarray, scaler_y: MinMaxScaler) -> np.ndarray:
    y_log = scaler_y.inverse_transform(y_norm)
    return np.expm1(y_log)

