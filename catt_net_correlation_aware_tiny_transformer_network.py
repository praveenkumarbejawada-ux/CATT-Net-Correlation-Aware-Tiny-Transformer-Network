def save_history_plot(history: Dict[str, List[float]], save_path: str, title: str) -> None:
    plt.figure(figsize=(8, 5))
    if "loss" in history:
        plt.plot(history["loss"], label="Train Loss")
    if "val_loss" in history:
        plt.plot(history["val_loss"], label="Val Loss")
    plt.xlabel("Epoch")
    plt.ylabel("Loss")
    plt.title(title)
    plt.legend()
    plt.tight_layout()
    plt.savefig(save_path, dpi=300)
    plt.close()


def plot_results_bar(df_results: pd.DataFrame, save_dir: str, prefix: str) -> None:
    for metric in ["MAE", "RMSE", "R2", "sMAPE_percent"]:
        plt.figure(figsize=(10, 5))
        plt.bar(df_results["variant"], df_results[metric])
        plt.xticks(rotation=20, ha="right")
        plt.ylabel(metric)
        plt.title(f"{prefix} - {metric}")
        plt.tight_layout()
        plt.savefig(os.path.join(save_dir, f"{prefix.lower().replace(' ', '_')}_{metric}.png"), dpi=300)
        plt.close()

