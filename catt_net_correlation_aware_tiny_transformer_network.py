class LearnablePositionalEmbedding(layers.Layer):
    def __init__(self, seq_len: int, d_model: int, **kwargs):
        super().__init__(**kwargs)
        self.seq_len = seq_len
        self.d_model = d_model
        self.pos_emb = layers.Embedding(input_dim=seq_len, output_dim=d_model)

    def call(self, x: tf.Tensor) -> tf.Tensor:
        positions = tf.range(start=0, limit=self.seq_len, delta=1)
        return x + self.pos_emb(positions)

    def get_config(self) -> Dict:
        config = super().get_config()
        config.update({"seq_len": self.seq_len, "d_model": self.d_model})
        return config


class AttentionPooling(layers.Layer):
    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.score_dense = layers.Dense(1)

    def call(self, x: tf.Tensor) -> tf.Tensor:
        scores = self.score_dense(x)
        weights = tf.nn.softmax(scores, axis=1)
        return tf.reduce_sum(weights * x, axis=1)


def transformer_encoder_block(
    x: tf.Tensor,
    d_model: int,
    num_heads: int,
    ff_dim: int,
    dropout: float,
) -> tf.Tensor:
    attn_input = layers.LayerNormalization(epsilon=1e-6)(x)
    attn_out = layers.MultiHeadAttention(
        num_heads=num_heads,
        key_dim=max(1, d_model // num_heads),
        dropout=dropout,
    )(attn_input, attn_input)
    attn_out = layers.Dropout(dropout)(attn_out)
    x = layers.Add()([x, attn_out])

    ff_input = layers.LayerNormalization(epsilon=1e-6)(x)
    ff = layers.Dense(ff_dim, activation="gelu")(ff_input)
    ff = layers.Dropout(dropout)(ff)
    ff = layers.Dense(d_model)(ff)
    ff = layers.Dropout(dropout)(ff)
    x = layers.Add()([x, ff])
    return x


def build_tiny_transformer(seq_len: int, num_features: int, num_outputs: int) -> Model:
    inputs = layers.Input(shape=(seq_len, num_features), name="input_seq")
    x = layers.Conv1D(CONV_FILTERS, kernel_size=5, padding="same", activation="gelu")(inputs)
    x = layers.Conv1D(CONV_FILTERS, kernel_size=3, padding="same", activation="gelu")(x)
    x = layers.Dense(D_MODEL)(x)
    x = LearnablePositionalEmbedding(seq_len, D_MODEL)(x)
    x = layers.Dropout(TRANSFORMER_DROPOUT)(x)

    for _ in range(NUM_TRANSFORMER_BLOCKS):
        x = transformer_encoder_block(x, D_MODEL, NUM_HEADS, FF_DIM, TRANSFORMER_DROPOUT)

    x_norm = layers.LayerNormalization(epsilon=1e-6)(x)
    avg_pool = layers.GlobalAveragePooling1D()(x_norm)
    max_pool = layers.GlobalMaxPooling1D()(x_norm)
    attn_pool = AttentionPooling()(x_norm)

    x = layers.Concatenate()([avg_pool, max_pool, attn_pool])
    x = layers.Dense(HEAD_DENSE_1, activation="gelu")(x)
    x = layers.Dropout(0.20)(x)
    x = layers.Dense(HEAD_DENSE_2, activation="gelu")(x)
    x = layers.Dropout(0.10)(x)
    outputs = layers.Dense(num_outputs, name="pollutant_forecast")(x)

    model = Model(inputs, outputs, name="ImprovedTinyTransformer_CorrGAN")
    model.compile(
        optimizer=tf.keras.optimizers.AdamW(learning_rate=LEARNING_RATE, weight_decay=1e-5),
        loss=tf.keras.losses.Huber(delta=1.0),
        metrics=[tf.keras.metrics.MeanAbsoluteError(name="mae")],
    )
    return model


def build_ablation_transformer(
    seq_len: int,
    num_features: int,
    num_outputs: int,
    use_conv: bool = True,
    num_blocks: int = 3,
    use_attention_pool: bool = True,
) -> Model:
    inputs = layers.Input(shape=(seq_len, num_features), name="input_seq")

    if use_conv:
        x = layers.Conv1D(CONV_FILTERS, kernel_size=5, padding="same", activation="gelu")(inputs)
        x = layers.Conv1D(CONV_FILTERS, kernel_size=3, padding="same", activation="gelu")(x)
    else:
        x = inputs

    x = layers.Dense(D_MODEL)(x)
    x = LearnablePositionalEmbedding(seq_len, D_MODEL)(x)
    x = layers.Dropout(TRANSFORMER_DROPOUT)(x)

    for _ in range(num_blocks):
        x = transformer_encoder_block(x, D_MODEL, NUM_HEADS, FF_DIM, TRANSFORMER_DROPOUT)

    x_norm = layers.LayerNormalization(epsilon=1e-6)(x)
    avg_pool = layers.GlobalAveragePooling1D()(x_norm)
    max_pool = layers.GlobalMaxPooling1D()(x_norm)

    if use_attention_pool:
        x = layers.Concatenate()([avg_pool, max_pool, AttentionPooling()(x_norm)])
    else:
        x = layers.Concatenate()([avg_pool, max_pool])

    x = layers.Dense(HEAD_DENSE_1, activation="gelu")(x)
    x = layers.Dropout(0.20)(x)
    x = layers.Dense(HEAD_DENSE_2, activation="gelu")(x)
    x = layers.Dropout(0.10)(x)
    outputs = layers.Dense(num_outputs)(x)

    model = Model(inputs, outputs, name="AblationTinyTransformer_CorrGAN")
    model.compile(
        optimizer=tf.keras.optimizers.AdamW(learning_rate=LEARNING_RATE, weight_decay=1e-5),
        loss=tf.keras.losses.Huber(delta=1.0),
        metrics=[tf.keras.metrics.MeanAbsoluteError(name="mae")],
    )
    return model


