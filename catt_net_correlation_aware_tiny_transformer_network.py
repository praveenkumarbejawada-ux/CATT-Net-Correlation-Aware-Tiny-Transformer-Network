def representative_data_gen(X_calib: np.ndarray):
    for i in range(min(200, len(X_calib))):
        yield [X_calib[i:i + 1].astype(np.float32)]


def export_tflite_int8(model: Model, X_calib: np.ndarray, save_path: str) -> None:
    converter = tf.lite.TFLiteConverter.from_keras_model(model)
    converter.optimizations = [tf.lite.Optimize.DEFAULT]
    converter.representative_dataset = lambda: representative_data_gen(X_calib)
    converter.target_spec.supported_ops = [tf.lite.OpsSet.TFLITE_BUILTINS_INT8]
    converter.inference_input_type = tf.int8
    converter.inference_output_type = tf.int8

    try:
        tflite_model = converter.convert()
    except Exception as e:
        raise RuntimeError(
            f"Pure INT8 conversion failed for model '{model.name}'. Original error: {e}"
        )

    with open(save_path, "wb") as f:
        f.write(tflite_model)


def tflite_predict_int8(tflite_path: str, X_test: np.ndarray) -> np.ndarray:
    interpreter = tf.lite.Interpreter(model_path=tflite_path)
    interpreter.allocate_tensors()

    input_details = interpreter.get_input_details()[0]
    output_details = interpreter.get_output_details()[0]

    in_scale, in_zero = input_details["quantization"]
    out_scale, out_zero = output_details["quantization"]

    preds = []
    for i in range(len(X_test)):
        x = X_test[i:i + 1].astype(np.float32)
        x_q = np.round(x / in_scale + in_zero).astype(np.int8)
        interpreter.set_tensor(input_details["index"], x_q)
        interpreter.invoke()
        y_q = interpreter.get_tensor(output_details["index"])
        y = (y_q.astype(np.float32) - out_zero) * out_scale
        preds.append(y[0])

    return np.asarray(preds, dtype=np.float32)


def save_keras_and_int8_results(
    model: Model,
    model_name: str,
    X_train: np.ndarray,
    X_test: np.ndarray,
    y_test: np.ndarray,
    scaler_y: MinMaxScaler,
    target_cols: List[str],
    save_dir: str,
) -> Tuple[Dict, Optional[Dict]]:
    os.makedirs(save_dir, exist_ok=True)

    keras_path = os.path.join(save_dir, f"{model_name}.keras")
    int8_path = os.path.join(save_dir, f"{model_name}_int8.tflite")
    model.save(keras_path)

    y_true = inverse_transform_targets(y_test, scaler_y)

    y_pred_keras_norm = model.predict(X_test, verbose=0)
    y_pred_keras = inverse_transform_targets(y_pred_keras_norm, scaler_y)
    keras_metrics = evaluate_per_target(y_true, y_pred_keras, target_cols)

    with open(os.path.join(save_dir, f"{model_name}_keras_metrics.json"), "w") as f:
        json.dump(keras_metrics, f, indent=2)
    metrics_dict_to_dataframe(keras_metrics).to_csv(os.path.join(save_dir, f"{model_name}_keras_metrics.csv"), index=False)

    int8_metrics = None
    try:
        export_tflite_int8(model, X_train[: min(200, len(X_train))], int8_path)
        y_pred_tflite_norm = tflite_predict_int8(int8_path, X_test)
        y_pred_tflite = inverse_transform_targets(y_pred_tflite_norm, scaler_y)
        int8_metrics = evaluate_per_target(y_true, y_pred_tflite, target_cols)

        with open(os.path.join(save_dir, f"{model_name}_int8_metrics.json"), "w") as f:
            json.dump(int8_metrics, f, indent=2)
        metrics_dict_to_dataframe(int8_metrics).to_csv(os.path.join(save_dir, f"{model_name}_int8_metrics.csv"), index=False)
    except Exception as e:
        error_info = {"model_name": model_name, "error": str(e)}
        with open(os.path.join(save_dir, f"{model_name}_int8_error.json"), "w") as f:
            json.dump(error_info, f, indent=2)
        print(f"\n[WARNING] INT8 export failed for {model_name}: {e}")

    print(f"\n===== {model_name} FP32 METRICS =====")
    print(json.dumps(keras_metrics, indent=2))
    if int8_metrics is not None:
        print(f"\n===== {model_name} INT8 METRICS =====")
        print(json.dumps(int8_metrics, indent=2))

    return keras_metrics, int8_metrics

